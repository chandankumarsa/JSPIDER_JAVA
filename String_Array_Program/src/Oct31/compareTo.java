package Oct31;

public class compareTo {
	public static void main(String[] args) {
		String s1="bala";
		String s2="baba";
		System.out.println(s1.compareTo(s2));
	}

}


/*This Java code compares two words: "bala" and "baba". It uses compareTo to see which word comes first in alphabetical order.
If "bala" is before "baba", it shows a negative number.
If they are the same, it shows zero.
If "bala" is after "baba", it shows a positive number.
In this case, it will show a positive number because "bala" comes after "baba".
*/